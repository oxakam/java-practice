package java_basics;

public class Variables {

	public static void main(String[] args) {

		int x = 23;	
		System.out.println(x);
		
		String words = "hej du der";		
		System.out.println(words);
		
		//other variables
		int var = 1000000000;					//integer
		long bigNumber = 1000000000000000000L; 	//L to the end indicates Long		
		short smallNumber = 32767;				//max value of short, min value -32768		
		byte verySmallNumber = 127;				//max value of byte, min value -128
		double decimalVar = 394.003;			//decimal value
		boolean decision = true;				//true, false
		char letter = '%';						//single char
		
	}

}
